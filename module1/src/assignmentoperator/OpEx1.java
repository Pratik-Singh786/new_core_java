package assignmentoperator;
public class OpEx1 {

	public static void main(String[] args) 
	{
	   int a=10;
	   int b=12;
	   int e=10;
	   int f=5;
	   boolean d=true;
	   boolean c=false;
	   System.out.println(~a);
	   System.out.println(~b);
	   System.out.println(!d);
	   System.out.println(!c);//negation(~) and(!)
	   
	   System.out.println(e+f); //java arithmetic operator
	   System.out.println(e-f);
	   System.out.println(e*f);
	   System.out.println(e%f);
	   System.out.println(e/f);
	   

	}

}
