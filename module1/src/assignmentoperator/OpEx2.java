package assignmentoperator;
public class OpEx2 {

	public static void main(String[] args) 
	{
		System.out.println(10<<2);//10*2^2
		System.out.println(10<<3); //left shift and right shift
		System.out.println(20>>2);//20/2^2
		System.out.println(15<<2);//15*2^2
		System.out.println(20>>>2);//20*2^2
		System.out.println(-20>>2);
		System.out.println(-20>>>2);
		

	}

}
