package flowcontrol;

public class switchex5 {

	public static void main(String[] args) 
	{
		int x=10;
		switch(x)
		{
		case 97:
			System.out.println(97);
		    break;
		case 98:
			System.out.println(98);
		    break;
		/*case 'a':                   1.  a is same as 97 so case a is not allowed because its duplicate case level
			System.out.println("a");  2.  the value should be in range of switch argument type.
			                          3.Duplicate case labels are not allowed
		    break;*/
			
		}
	  
	  
	  
	  

	}

}
